    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String user= username.getText();
        String p= pass.getText();
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/cms1","root", "");
            Statement stmt= conn.createStatement();
            String sql="Select * from loginpage";
            
            ResultSet rs= stmt.executeQuery(sql);
            while(rs.next())
            {
                String usern= rs.getString("user_id");  //user_id is the column name in the table login
                String pass= rs.getString("passwd");  //Password is the column name in the table login
                
                if(user.equals(usern) && p.equals(pass))
                {
                    new Welcome().setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Username or Password is incorrect!!");
                }
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error occured while establishing the connection");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

