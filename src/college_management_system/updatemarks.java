    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/cms1","root", "");

            PreparedStatement pstmt= conn.prepareStatement("SELECT * FROM exam_result WHERE StudentID=?");
            pstmt.setString(1, sid.getText());

            ResultSet rs= pstmt.executeQuery();
            while(rs.next())
            {
                dbms.setText(rs.getString("DataBase Management System"));
                ds.setText(rs.getString("Data Structure"));
                oop.setText(rs.getString("Object Oriented Programming"));
                aem.setText(rs.getString("Applied Engineering Mathematics"));
                ed.setText(rs.getString("Entrepreneurship Development"));
                total.setText(rs.getString("Total(Out of 500)"));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

