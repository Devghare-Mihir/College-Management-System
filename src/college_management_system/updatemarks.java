    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String sid1= sid.getText();
        String dbms1= dbms.getText();
        String ds1= ds.getText();
        String aem1= aem.getText();
        String oop1= oop.getText();
        String ed1= ed.getText();
        String total1= total.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/cms1","root", "");
            
            PreparedStatement pstmt= conn.prepareStatement("UPDATE exam_result SET "
            + "`DataBase Management System`='"+dbms1+"',"
            + "`Data Structure`='"+ds1+"',"
            + "`Applied Engineering Mathematics`='"+aem1+"',"
            + "`Object Oriented Programming`='"+oop1+"',"
            + "`Entrepreneurship Development`='"+ed1+"',"
            + "`Total(Out of 500)`="+("`DataBase Management System`+`Data Structure`"
                    + "+`Applied Engineering Mathematics`+`Object Oriented Programming`"
                    + "+`Entrepreneurship Development`")+" WHERE StudentID= "+sid1);
            
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data has been updated Successfully!!");
            conn.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_updateActionPerformed

