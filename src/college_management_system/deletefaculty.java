    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn= DriverManager.getConnection("jdbc:mysql://localhost:3306/cms1","root", "");

            PreparedStatement pstmt= conn.prepareStatement("SELECT * FROM teacher_reg WHERE    `Teacher ID`=?");
            pstmt.setString(1, Id.getText());

            ResultSet rs= pstmt.executeQuery();
            if(rs.next())
            {
                id.setText(rs.getString("Teacher Id"));
                fname.setText(rs.getString("First Name"));
                mname.setText(rs.getString("Middle Name"));
                lname.setText(rs.getString("Last Name"));
                email.setText(rs.getString("Email"));
                gender.setText(rs.getString("Gender"));
                fphone.setText(rs.getString("Phone No."));
                adhr.setText(rs.getString("Aadhar"));
                qualification.setText(rs.getString("Qualification"));
                status.setText(rs.getString("Status"));
                date.setText(rs.getString("Date of Joining"));
                subject.setText(rs.getString("Subject"));
                address.setText(rs.getString("Address"));
            }
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

